package day02;

public class VariableAndConcatenation {
    public static void main(String[] args) {

        int hourOfCoding = 10 ;
        int myBirthYear = 1982 ;
        int myAge = 37 ;
        int myFamilyMemberCount = 4 ;
        int myMonitorCount = 2 ;
        int myAreaCode = 95662 ;
        int myDesiredSalary = 120 ;

        System.out.println("The hour of coding is " + hourOfCoding + " hours.");
        System.out.println("My birth year: " + myBirthYear + ".");
        System.out.println("I am " + myAge + " years old.");
        System.out.println("My family consists of " + myFamilyMemberCount + " person.");
        System.out.println("I have " + myMonitorCount + " monitors to study.");
        System.out.println("My area code is " + myAreaCode + ".");
        System.out.println("My desired salary is $" + myDesiredSalary + "K+ per year.");

    }

}
