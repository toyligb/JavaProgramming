package day03;

public class StringDataType {

    public static void main(String[] args) {

        String firstName = "Toyly";
        String liveCity = "Sacramento";
        char firstLetterOfSurname = 'G';
        int myAge = 37;
        double myHeight = 5.12D;
        boolean isMarried = true;
        byte childrenCount = 2;
        float mathPiNumber = 3.14F;

        System.out.print("My name is " + firstName + ". ");
        System.out.print("First letter of my surname is " + firstLetterOfSurname + ". ");
        System.out.print("I live in " + liveCity + ". ");
        System.out.print("I am " + myAge + " years old. My height is " + myHeight + ". ");
        System.out.print("Am I married? : " + isMarried + ". I have " + childrenCount + " kids. ");
        System.out.print("Math Pi number is " + mathPiNumber + ".");

    }

}
